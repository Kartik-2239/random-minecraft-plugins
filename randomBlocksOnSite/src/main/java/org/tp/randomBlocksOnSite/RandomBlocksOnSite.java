    private final Map<Player, Location> lastLoc = new HashMap<>();

    @Override
    public void onEnable() {
        // Plugin startup logic
        getServer().getPluginManager().registerEvents(this,this);
    }

    @EventHandler
    public void onMovement(PlayerMoveEvent event){
        Location previousLoc = lastLoc.get(event.getPlayer());
        Player p = event.getPlayer();
        World world = p.getWorld();
        Block curBlock = p.getTargetBlock(null,100);
        if (curBlock.getBlockData() == Material.OBSIDIAN.createBlockData())return;
        if (curBlock.getBlockData() == Material.NETHER_PORTAL.createBlockData())return;
        if (curBlock.getBlockData() == Material.END_PORTAL_FRAME.createBlockData())return;
        Location loc= curBlock.getLocation();
        if(loc.equals(previousLoc)) return;
        lastLoc.put(p,loc);
        Material b = blocks.get(random.nextInt(0, blocks.toArray().length - 1));
        if (b.isBlock()){
            BlockData blockData = b.createBlockData();
            world.setBlockData(loc, blockData);
        }
    }

